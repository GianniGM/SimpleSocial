package simpleSocialShared;

public class SimpleSocialException extends Exception{

	private static final long serialVersionUID = -4854696937591108309L;

	public SimpleSocialException() { super(); }
	public SimpleSocialException(String message) { super(message); }
	public SimpleSocialException(String message, Throwable cause) { super(message, cause); }
	public SimpleSocialException(Throwable cause) { super(cause); }

}
